  printf("SYSCLOCK: %lu Hz\n", HAL_RCC_GetSysClockFreq());
  printf("HCLOCK  : %lu Hz\n", HAL_RCC_GetHCLKFreq());
  printf("P1CLOCK : %lu Hz\n", HAL_RCC_GetPCLK1Freq());
  printf("P2CLOCK : %lu Hz\n", HAL_RCC_GetPCLK2Freq());
  terminal_init("led $");


  //LED Lamp test
  {
  uint8_t number = 0;
  for(int k = 0 ; k  < 10; k++)
  {
	  HAL_Delay(300);
	  led_set_time(number, number);
	  led_set_temperature((float)k * 11.1);
	  number += 11;
  }
  HAL_Delay(300);
  }

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  uint32_t tick = 0;
  float previous_temperature = 0;
